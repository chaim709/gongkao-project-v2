def get_subcategories(subject: str) -> list[str]:
    """获取某科目的所有二级分类"""
    return list(KNOWLEDGE_TAGS.get(subject, {}).keys())


def get_knowledge_points(subject: str, subcategory: str) -> list[str]:
    """获取某二级分类下的所有知识点"""
    return KNOWLEDGE_TAGS.get(subject, {}).get(subcategory, [])


def get_all_flat_tags() -> list[dict]:
    """获取所有标签的扁平列表，用于前端选择器"""
    result = []
    for subject, subcats in KNOWLEDGE_TAGS.items():
        for subcat, points in subcats.items():
            for point in points:
                result.append({
                    "subject": subject,
                    "subcategory": subcat,
                    "knowledge_point": point,
                    "label": f"{subject} > {subcat} > {point}",
                })
    return result
